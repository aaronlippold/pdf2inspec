loaded into each
    manager node's memory. You should protect the mutual TLS encryption key and
the key
    used to encrypt and decrypt Raft logs at rest. This protection could be
enabled by
    initializing swarm with --autolock flag.
    With --autolockenabled, when Docker restarts, you must unlock the swarm
first, using a
    key encryption key generated by Docker when the swarm was initialized.

